package cn.emagsoftware.telephony;

public class ReflectHiddenFuncException extends Exception
{
    /**
     * 
     */
    private static final long serialVersionUID = 1L;

    public ReflectHiddenFuncException()
    {
        super();
    }

    public ReflectHiddenFuncException(String message)
    {
        super(message);
    }

    public ReflectHiddenFuncException(String message, Throwable cause)
    {
        super(message, cause);
    }

    public ReflectHiddenFuncException(Throwable cause)
    {
        super(cause);
    }

}
